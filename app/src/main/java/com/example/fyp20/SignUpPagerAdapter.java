package com.example.fyp20;

import androidx.annotation.NonNull;
import androidx.fragment.app.Fragment;
import androidx.fragment.app.FragmentActivity;
import androidx.viewpager2.adapter.FragmentStateAdapter;

import java.util.List;

public class SignUpPagerAdapter extends FragmentStateAdapter {
    private final List<SignUpFragment> fragments;

    public SignUpPagerAdapter(@NonNull FragmentActivity fragmentActivity, List<SignUpFragment> fragments) {
        super(fragmentActivity);
        this.fragments = fragments;
    }

    @NonNull
    @Override
    public Fragment createFragment(int position) {
        return fragments.get(position);
    }

    @Override
    public int getItemCount() {
        return fragments.size();
    }
}